int	ft_is_prime(int nb)
{
	int	x;

	if (nb <= 1)
		return (0);
	if (nb == 2147483647)
		return (1);
	x = 1;
	while (x <= nb)
	{
		if (nb % x == 0)
			if (x != 1 && x != nb)
				return (0);
		x++;
	}
	return (1);
}
