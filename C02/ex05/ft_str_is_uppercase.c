int	ft_str_is_uppercase(char *str)
{
	int	x;
	int	i;

	x = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
		{
			x = 0;
		}
		i++;
	}
	return (x);
}
